    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        boolean resultado = false;
        int codigo=Integer.parseInt(txtCodigo.getText());
        String cedula= txtCedula.getText();
        String nombre= txtNombre.getText();
        String Apellido=txtApellido.getText();
        String direccion = txtDireccion.getText();
        String correo=txtCorreo.getText();
        String contra=txtPwd.getText();
        try{
            Constructor<Usuario> constructor = Usuario.class.getDeclaredConstructor();
            constructor.setAccessible(true);
            Usuario u = constructor.newInstance();
            u.setCorreo(correo);
            if (cedula.isEmpty() || nombre.isEmpty() || Apellido.isEmpty() || correo.isEmpty() || contra.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Obligatorio llenar todos los campos");
            } else {
                if (controladorUsuario.leer(u) != null) {
                    u.setCodigo(codigo);
                    u.setCedula(cedula);
                    u.setNombre(nombre);
                    u.setApellido(Apellido);
                    u.setDireccion(direccion);
                    u.setCorreo(correo);
                    u.setContraseña(contra);
                    resultado=controladorUsuario.actualizar(u);
                }else{
                    u.setCodigo(codigo);
                    u.setCedula(cedula);
                    u.setNombre(nombre);
                    u.setApellido(Apellido);
                    u.setDireccion(direccion);
                    u.setCorreo(correo);
                    u.setContraseña(contra);
                    resultado=controladorUsuario.crear(u);
                }
                limpiar();
                cargarDatos();
                dispose();
            }
            
        }catch (InstantiationException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalArgumentException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvocationTargetException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchMethodException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SecurityException ex) {
            Logger.getLogger(GestionUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCrearActionPerformed

