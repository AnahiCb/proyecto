    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblUsuarios.getModel();

        modeloTabla.setRowCount(0);
        for (Usuario usuario : controladorUsuario.getListaGenerica()) {
            Object[] rowData = {usuario.getCodigo(),usuario.getCedula(), usuario.getNombre(),usuario.getApellido(),usuario.getDireccion(),usuario.getCorreo(),usuario.getContraseña()};
            modeloTabla.addRow(rowData);
        }
        tblUsuarios.setModel(modeloTabla);
    }

